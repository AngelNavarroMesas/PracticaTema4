package ejercicio4;

public class Operaciones {
	
	public int num1, num2;
	
	void suma() {
		System.out.println(num1+"+"+num2+"="+(num1+num2));
	}
	
	void resta() {
		System.out.println(num1+"-"+num2+"="+(num1-num2));
	}
	
	void multiplicacion() {
		System.out.println(num1+"*"+num2+"="+(num1*num2));
	}
	
	void division() {
		System.out.println(num1+"/"+num2+"="+(num1/num2));
	}
	
}
